/*  English Label mouse hover  */
$(document).ready(function () {
    $("#English").mouseover(function () {
        $("#colEng").addClass("theplan hover1");
        $("#Chat_EN").addClass("theplan hover1");

    });
    $("#English").mouseout(function () {
        $("#colEng").removeClass("theplan hover1");
        $("#Chat_EN").removeClass("theplan hover1");

    });
});


